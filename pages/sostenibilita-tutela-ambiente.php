      <h4>6.3 Gestione dell’acqua</h4>
      <p>L’acqua riveste un ruolo centrale nei processi produttivi e nel garantire elevati standard igienico-sanitari. Le acque utilizzate vengono depurate internamente e restituite all’ambiente nel rispetto della normativa vigente, con migliaia di analisi annuali per il controllo dei parametri chimici, fisici e biologici. L’installazione di misuratori nei punti strategici consente di monitorare i consumi e prevenire sprechi, mentre gli investimenti tecnologici assicurano l’adozione continua delle migliori soluzioni disponibili.</p>

      <h4>6.4 Energia rinnovabile e riduzione delle emissioni</h4>
      <p>La strategia energetica è orientata allo sviluppo di fonti a basso impatto ambientale, attraverso impianti di digestione anaerobica e fotovoltaici. Il biogas prodotto dal trattamento dei reflui industriali copre una quota significativa del fabbisogno energetico aziendale e viene utilizzato sia per la produzione di energia elettrica sia per i processi termici. Complessivamente, gli impianti di cogenerazione e fotovoltaici consentono di evitare migliaia di tonnellate di CO₂ ogni anno e di immettere in rete energia sufficiente a coprire il fabbisogno di migliaia di famiglie.

