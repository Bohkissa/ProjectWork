      <p>La tutela dell’ambiente è parte integrante della strategia aziendale e si traduce in un impegno quotidiano per una gestione responsabile delle risorse naturali, la riduzione degli impatti ambientali e il miglioramento continuo delle performance. L’approccio adottato coinvolge tutti i processi aziendali ed è coerente con una cultura produttiva orientata alla circolarità, all’efficienza e alla prevenzione degli sprechi. Questo percorso è supportato dall’adesione a schemi di certificazione riconosciuti a livello internazionale: nel 2023, 16 su 18 siti produttivi alimentari risultano certificati ISO 14001, con l’estensione a ulteriori stabilimenti e l’avvio del percorso di certificazione per i mangimifici.</p>

