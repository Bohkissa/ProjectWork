      <h4>6.5 Economia circolare e gestione dei rifiuti</h4>
      <p>La gestione dei rifiuti si basa su principi di economia circolare, con l’obiettivo di massimizzare il recupero di materia ed energia. Oltre l’80% dei rifiuti prodotti è destinato a riciclo, compostaggio o recupero, mentre i rifiuti pericolosi rappresentano una quota minima e sono gestiti nel pieno rispetto delle normative. Progetti specifici di recupero e valorizzazione degli scarti produttivi permettono di trasformare i rifiuti in risorse per altre filiere industriali.</p>

