      <h4>6.1 Biodiversità e tutela del territorio</h4>
      <p>L’azienda contribuisce attivamente alla salvaguardia della biodiversità attraverso la gestione di oltre 1.700 ettari di terreni agricoli, boschivi e prati stabili. Queste aree svolgono una funzione chiave di conservazione degli ecosistemi locali, di assorbimento della CO₂ e di mantenimento della bio-permeabilità territoriale. Grazie al censimento di oltre 23.000 piante e alla gestione sostenibile del patrimonio arboreo e agricolo, si stima una capacità di sequestro di circa 14.200 tonnellate di CO₂ all’anno, senza interferenze con habitat di interesse comunitario.</p>

