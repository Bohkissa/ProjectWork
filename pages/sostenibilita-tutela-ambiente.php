      <h4>6.2 Monitoraggio ambientale e gestione delle risorse</h4>
      <p>Il Sistema di Gestione Integrato Qualità–Sicurezza–Ambiente consente un controllo puntuale delle principali matrici ambientali – acqua, energia e rifiuti – su tutti i siti produttivi. Il monitoraggio è supportato dal “Cruscotto Ambientale”, uno strumento che permette di analizzare indicatori chiave e individuare le Migliori Tecniche Disponibili per ridurre impatti e costi ambientali. Gli interventi includono l’efficientamento energetico degli impianti, l’ottimizzazione dei processi produttivi e la riduzione delle dispersioni.</p>

